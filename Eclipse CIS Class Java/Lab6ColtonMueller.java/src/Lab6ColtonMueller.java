
import java.util.Scanner;

public class Lab6ColtonMueller {

	public static void main(String[] args) {
		
Scanner sc = new Scanner(System.in);
		
		int number;
	
		System.out.print("Please enter an integer between 10 and 12.");
		number = sc.nextInt(); 
		
		
		
		switch (number) {
		
		case 10 :  System.out.println("Ten"); break; 
		case 11 :  System.out.println("Eleven"); break; 
		case 12 :  System.out.println("Twelve"); break; 
		default :  System.out.println("Enter one of the numbers - 10, 11, or 12."); 
		
		
		
		}
			
		
		
		
		

	}

}
